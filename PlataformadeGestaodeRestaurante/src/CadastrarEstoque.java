    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // 1- pegar os dados digitados e guardar em vari√°veis
        String id,i,q,d;
        id = txtIDp.getText();
        i = txtItem.getText();
        q = txtQuantidade.getText();
        d = cmbDepartamento.getSelectedItem().toString();
        try {
            // 2- Conectar com o DB restaurante
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectar = DriverManager.getConnection("jdbc:mysql://localhost:3306/restaurante","root","");
            // 3- Enviar os dados para tabela do BD
            var st = conectar.prepareStatement("INSERT INTO estoque (idproduto,item,quantidade,departamento)VALUES(?,?,?,?)");
            st.setString(1, id);
            st.setString(2, i);
            st.setString(3, q);
            st.setString(4, d);
            st.executeUpdate(); // executa o comando INSERT
            // 4- exibir uma mensagem para o usuario
            JOptionPane.showMessageDialog(null,"Item cadastrado com sucesso");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null,"Entre em contato com o suporte e informe o erro:" + ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Entre em contato com o suporte e informe o erro:" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

