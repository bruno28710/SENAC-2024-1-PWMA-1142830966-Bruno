    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // 1- Obter o nome do item a ser alterado
        String i;
        i = txtMesas.getText().toString();
        try {
    // 2- Conectar com o DB restaurante
    Class.forName("com.mysql.cj.jdbc.Driver");
    Connection conectar = DriverManager.getConnection("jdbc:mysql://localhost:3306/restaurante", "root", "");

    // 3- Verificar se o idmesa já está cadastrado na tabela reservas
    var st = conectar.prepareStatement("SELECT idmesa FROM reservas WHERE idmesa = ?");
    st.setString(1, i);
    ResultSet rs = st.executeQuery(); // executa o comando SELECT

    // 4- Se o idmesa não estiver cadastrado, inserir na tabela reservas
    if (rs.next()) {
        JOptionPane.showMessageDialog(null, "Mesa já está reservada");
    } else {
        // Inserir o idmesa na tabela reservas
        var su = conectar.prepareStatement("INSERT INTO reservas (idmesa) VALUES (?)");
        su.setString(1, i);
        // 5- Passar o idmesa para a outra tela
        new ControleReservas(i).setVisible(true);
        dispose(); 
    }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null,"Entre em contato com o suporte e informe o erro:" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

