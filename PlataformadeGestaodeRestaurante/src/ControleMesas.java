    public void controleMesas() {
        try {
            // 1- Conexão com o BD
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectar = DriverManager.getConnection("jdbc:mysql://localhost:3306/restaurante","root","");
            // 2- Buscar todos os dados que eu quero exibir na tela
            var st = conectar.prepareStatement("SELECT mesas.idmesa, reservas.nome \n" +"FROM mesas \n" +"LEFT JOIN reservas ON mesas.idmesa = reservas.idmesa");
            ResultSet mesas = st.executeQuery(); // essa linha executa o SELECT
            DefaultTableModel tblModelo = (DefaultTableModel) tblMesas.getModel();
            tblModelo.setRowCount(0);
            // passa linha por linha até o final da tabela
            while(mesas.next()){
                String linha[]={
                    mesas.getString("idmesa"),
                    mesas.getString("nome"),
                };
                tblModelo.addRow(linha);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null,"Entre em contato com o suporte e informe o erro:" + ex.getMessage());
        }
    }

